# Задане чотиризначне натуральне число
n = int(input("Введіть чотиризначне натуральне число: "))

# Перетворюємо число на рядок, щоб обробити його цифри
number_str = str(n)

# Ініціалізуємо змінну для збереження найбільшої цифри
max_digit = 0

# Проходимося по кожній цифрі в рядку
for digit in number_str:
    # Перетворюємо цифру назад у ціле число
    digit_int = int(digit)
    # Порівнюємо поточну цифру з максимальною
    if digit_int > max_digit:
        max_digit = digit_int

# Виводимо результат
print("Найбільша цифра у числі", n, ":", max_digit)
